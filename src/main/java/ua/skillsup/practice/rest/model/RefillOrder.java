public class RefillOrder {

	private int dark;
	private int light;
	private int ale;

	public int getDark() {
		return dark;
	}

	public void setDark(int dark) {
		this.dark = dark;
	}

	public int getLight() {
		return light;
	}

	public void setLight(int light) {
		this.light = light;
	}

	public int getAle() {
		return ale;
	}

	public void setAle(int ale) {
		this.ale = ale;
	}
}
